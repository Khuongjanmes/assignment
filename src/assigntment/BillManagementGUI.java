    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
        String code = txtCustomerCode.getText().toUpperCase();
        String type = txtCustomerType.getText();
        int oldIndex = Integer.parseInt(txtOldIndex.getText());
        int newIndex = Integer.parseInt(txtNewIndex.getText());

        if (oldIndex < 0 || newIndex < 0 || oldIndex > newIndex) {
            throw new NumberFormatException("Invalid Index Values");
        }
        if (billManager.isCustomerCodeExists(code)) {
            JOptionPane.showMessageDialog(this, "Customer code already exists. Cannot add duplicate bill.");
            return;
        }
        Bill bill = new Bill(code, type, oldIndex, newIndex);
        boolean added = billManager.addBill(bill);
        if (added) {
            outputArea.append("Bill added successfully:\n" + bill.toString() + "\n");
            outputArea.append("-----------------------------\n");
        } else {
            outputArea.append("Failed to add bill. Customer code already exists.\n");
        }
        clearFields();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input. Please check the values.");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String code = txtCustomerCode.getText();
        Bill foundBill = billManager.findBillByCode(code);

        if (foundBill != null) {
            outputArea.setText("Bill found:\n" + foundBill.toString() + "\n");
        } else {
            outputArea.setText("No bill found with the entered customer code.\n");
        }
        clearFields();
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String code = txtCustomerCode.getText();
        boolean billDeleted = billManager.deleteBillByCode(code);
        if (billDeleted) {
            outputArea.setText("Bill deleted successfully!\n");
            refreshOutputArea();
        } else {
            outputArea.setText("No bill found with the given customer code.\n");
        }
        clearFields();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPaymentActionPerformed
        String type = txtCustomerType.getText();
        int totalPayment = billManager.calculateTotalPaymentByCustomerType(type);
        outputArea.setText("Total payment for customer type " + type + ": " + totalPayment + " VND\n");
        clearFields();
    }//GEN-LAST:event_btnPaymentActionPerformed

    private void btnLargestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLargestActionPerformed
        Bill largestBill = billManager.findBillWithLargestPayment();
        if (largestBill != null) {
            outputArea.setText("Bill with the largest payment:\n" + largestBill.toString() + "\n");
        } else {
            outputArea.setText("No bills available.\n");
        }
        clearFields();
    }//GEN-LAST:event_btnLargestActionPerformed

