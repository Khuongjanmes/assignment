    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        outputArea.setText(""); 
       for (Bill bill : billManager.getAllBills().values()) {
            outputArea.append("Customer Code: " + bill.getCustomerCode() + "\n");
            outputArea.append("Customer Type: " + bill.getCustomerType() + "\n");
            outputArea.append("Old Index: " + bill.getOldIndex() + "\n");
            outputArea.append("New Index: " + bill.getNewIndex() + "\n");
            outputArea.append("Payment: " + bill.calculatePayment() + " VND\n");
            outputArea.append("-----------------------------\n");
        }
        btnBack.setVisible(true);
        btnAdd.setVisible(false);
        btnFind.setVisible(false);
        btnDelete.setVisible(false);
        btnPayment.setVisible(false);
        btnLargest.setVisible(false);
        btnShow.setVisible(false);
    }//GEN-LAST:event_btnShowActionPerformed

